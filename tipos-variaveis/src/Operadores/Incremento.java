package operadores;

public class Incremento {
    
    public static void main(String[] args) {
        //A incrementação está associada a recorrencia de atribuição de um mesmo valor
        int numero = 5;
        //x repetição
        numero++; // é a mesma representação de numero + 1

        System.out.println(numero);

        numero--; //Decremento

        System.out.println(numero);
    }
}
